def translate(language: str, key: str, **kwargs) -> str:
    """Return localized text with Polish key-fallback and optional interpolation.

    Preserves the former ``ShockerCalcApp._t`` behavior 1:1: an unknown language
    uses the ``pl`` dictionary; a missing key falls back to ``pl``; if absent there
    too, the key itself is returned. ``kwargs`` interpolate via ``str.format``.
    """
    text = I18N.get(language, I18N["pl"]).get(key, I18N["pl"].get(key, key))
    return text.format(**kwargs) if kwargs else text


def display_category(language: str, category: str | None) -> str:
    """Return the display label for a product category in the given language."""
    if not category:
        return ""
    if language == "en":
        return CATEGORY_LABELS_EN.get(category, category.replace("_", " "))
    return category.replace("_", " ")
